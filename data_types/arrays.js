// Arrays are used to store lists of items

let getColor = ['blue', 'green', 'black'];

// Print the array
console.log(getColor);

// Access specific elements
console.log(getColor[0]); // Outputs: blue (first element)

// Get the length of the array
console.log(getColor.length); // Outputs: 3

// Manipulating arrays using built-in functions
getColor.sort(); // Sort the array

// Print the sorted array
console.log(getColor);   // Outputs: ['black', 'blue', 'green'] (sorted array)

